package com.ProjMgmtSys.model.Gro;

public class Gro {
	private int groId;
	private String groName;
	private int depId;
	
	public int getGroId() {
		return groId;
	}
	public void setGroId(int groId) {
		this.groId = groId;
	}
	public String getGroName() {
		return groName;
	}
	public void setGroName(String groName) {
		this.groName = groName;
	}
	public int getDepId() {
		return depId;
	}
	public void setDepId(int depId) {
		this.depId = depId;
	}
	
}
