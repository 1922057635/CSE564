package com.ProjMgmtSys.model.Object;

public class Object {
	private int objId;
	private String objName;
	private int depId;
	private int groId;
	private int rowNO;
	
	public int getObjId() {
		return objId;
	}
	public void setObjId(int objId) {
		this.objId = objId;
	}
	public String getObjName() {
		return objName;
	}
	public void setObjName(String objName) {
		this.objName = objName;
	}
	public int getDepId() {
		return depId;
	}
	public void setDepId(int depId) {
		this.depId = depId;
	}
	public int getGroId() {
		return groId;
	}
	public void setGroId(int groId) {
		this.groId = groId;
	}
	public int getRowNO() {
		return rowNO;
	}
	public void setRowNO(int rowNO) {
		this.rowNO = rowNO;
	}
	
}
